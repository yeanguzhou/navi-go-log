type Stdio struct {
	w io.Writer
	l *sync.Mutex
}

func (stdio *Stdio) Write(data []byte) (n int, err error) {
	stdio.l.Lock()
	n, err = stdio.w.Write(data)
	stdio.l.Unlock()
	return
}

func GetLockWriter(w io.Writer, lock *sync.Mutex) io.Writer {
	return &Stdio{w, lock}
}

// 获取函数调用信息
func setFuncInfo(skip int) (fileName, module, funcFullName string, lineNo int) {
	fileName = "NIL"
	funcFullName = "NIL"
	module = "NIL"
	lineNo = -1

	pc, file, lineNo, ok := runtime.Caller(skip)
	if !ok {
		return
	}
	// 设置函数名和包名

	funcFullName = runtime.FuncForPC(pc).Name() // 900+ ns/op

	// 获取文件名
	_, fileName = filepath.Split(file)

	slashI := strings.Index(funcFullName, ".")
	if slashI == -1 {
		module = funcFullName
	} else {
		module = funcFullName[:slashI]
	}
	return
}

// 获取调用栈信息
func stackTrace(all bool) string {
	// Reserve 10K buffer at first
	buf := make([]byte, 10240)
	var size int
	for {
		size = runtime.Stack(buf, all)
		// The size of the buffer may be not enough to hold the stacktrace,
		// so double the buffer size
		if size == len(buf) {
			buf = make([]byte, len(buf)<<1)
			continue
		}
		break
	}
	return string(buf[:size])
}

// Copy by
// NOTE: keep in sync with stringBytes below.
func EncodeString(s string, escapeHTML bool) []byte {
	e := GetBytesBuffer()
	defer PutBytesBuffer(e)
	start := 0
	e.WriteByte('"')
	for i := 0; i < len(s); {
		if b := s[i]; b < utf8.RuneSelf {
			if htmlSafeSet[b] || (!escapeHTML && safeSet[b]) {
				i++
				continue
			}
			if start < i {
				e.WriteString(s[start:i])
			}
			switch b {
			case '\\', '"':
				e.WriteByte('\\')
				e.WriteByte(b)
			case '\n':
				e.WriteByte('\\')
				e.WriteByte('n')
			case '\r':
				e.WriteByte('\\')
				e.WriteByte('r')
			case '\t':
				e.WriteByte('\\')
				e.WriteByte('t')
			default:
				// This encodes bytes < 0x20 except for \t, \n and \r.
				// If escapeHTML is set, it also escapes <, >, and &
				// because they can lead to security holes when
				// user-controlled strings are rendered into JSON
				// and served to some browsers.
				e.WriteString(`\u00`)
				e.WriteByte(hex[b>>4])
				e.WriteByte(hex[b&0xF])
			}
			i++
			start = i
			continue
		}
		c, size := utf8.DecodeRuneInString(s[i:])
		if c == utf8.RuneError && size == 1 {
			if start < i {
				e.WriteString(s[start:i])
			}
			e.WriteString(`\ufffd`)
			i += size
			start = i
			continue
		}
		// U+2028 is LINE SEPARATOR.
		// U+2029 is PARAGRAPH SEPARATOR.
		// They are both technically valid characters in JSON strings,
		// but don't work in JSONP, which has to be evaluated as JavaScript,
		// and can lead to security holes there. It is valid JSON to
		// escape them, so we do so unconditionally.
		// See http://timelessrepo.com/json-isnt-a-javascript-subset for discussion.
		if c == '\u2028' || c == '\u2029' {
			if start < i {
				e.WriteString(s[start:i])
			}
			e.WriteString(`\u202`)
			e.WriteByte(hex[c&0xF])
			i += size
			start = i
			continue
		}
		i += size
	}
	if start < len(s) {
		e.WriteString(s[start:])
	}
	e.WriteByte('"')
	tmp := make([]byte, e.Len())
	copy(tmp, e.Bytes())
	return tmp
}

